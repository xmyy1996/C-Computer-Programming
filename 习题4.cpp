# include<iostream.h>

void main()
{ 
	int i;
	cin>>i;
	cout<<i;
	while(i != 1)
	{
		cout<<"->";
		if(i % 2)
		{
			i = i * 3 + 1;
		}
		else
		{
			i = i / 2;
		}
		cout<<i;
	}
	cout<<endl;
}
